    private void CadastrarHospedeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarHospedeActionPerformed
        // TODO add your handling code here:
        HospedeRepository hospedeRepository = new HospedeRepository();
        String cpf = this.cpf.getText();
        String nome = this.nome.getText();
        String endereco = cidade.getText()+", "+estado.getText()+", "+pais.getText();
        String telefone = this.telefone.getText();
        String email = this.email.getText();
        
        Hospede hospede = new Hospede(nome, cpf, endereco, telefone, email);
        System.out.println(hospede);
        
        hospedeRepository.insertHospede(hospede);
        
    }//GEN-LAST:event_CadastrarHospedeActionPerformed

